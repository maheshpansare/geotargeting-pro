	*
	*  @type	action
	*  @since	3.6
	*  @date	23/01/13
	*
	*  @param	$field	- an array holding all the field's data
	*/
	
	function create_options( $field )
	{
		// defaults?
		
		$field = array_merge($this->defaults, $field);
		
		
		// key is needed in the field names to correctly save the data
		$key = $field['name'];
		
		
		// Create Field Options HTML
		?>
<tr class="field_option field_option_<?php echo $this->name; ?>">
	<td class="label">
		<label><?php _e("Show Regions or Countries",'geot'); ?></label>
	</td>
	<td>
		<?php
		
		do_action('acf/create_field', array(
			'type'		=>	'radio',
			'name'		=>	'fields['.$key.'][geot_show]',
			'value'		=>	$field['geot_show'],
			'layout'	=>	'horizontal',
			'choices'	=>	array(
				'regions' => __('Regions' ,'geot'),
				'countries' => __('Countries', 'geot'),
			)
		));
		
		?>
	</td>
</tr>
		<?php
		
	}
	
	
	/*
	*  create_field()
	*
	*  Create the HTML interface for your field
	*
	*  @param	$field - an array holding all the field's data
	*
	*  @type	action
	*  @since	3.6
	*  @date	23/01/13
	*/
	
	function create_field( $field )
	{
		// defaults?
		
		$field = array_merge($this->defaults, $field);
		
		?>
		<div>
		<?php
		
		do_action('acf/create_field', array(
			'type'		=>	'radio',
			'name'		=>	$field['name'].'[geot_condition]',
			'value'		=>	$field['value']['geot_condition'],
			'layout'	=>	'horizontal',
			'choices'	=>	array(
				'exclude' => __('Exclude to' ,'geot'),
				'include' => __('Show to', 'geot'),
			)
		));
		
		if( 'regions' == $field['geot_show']) {
			
			$regions 	= apply_filters('geot/get_regions', array());
				
			if( is_array( $regions ) ) { 
				
				foreach ($regions as $r) {
					if (!empty( $r['name'] ) ) {
						$choices[$r['name']] = $r['name'];
					}	
				}

				do_action('acf/create_field', array(
					'type'		=>	'select',
					'multiple'	=>	true,
					'name'		=>	$field['name'].'[geot_regions]',
					'value'		=>	$field['value']['geot_regions'],
					'choices'	=>	$choices
				));				
			
			} else { ?>
			
				<p> Add some regions first.</p>
			
			<?php	
			}				
		} else {

			$countries 	= apply_filters('geot/get_countries', array());
			
			if( is_array( $countries ) ) { 
				
				foreach ($countries as $r) {
					if( !empty( $r->maxmind_country ) ) {

						$choices[$r->maxmind_country_code] = $r->maxmind_country;
						
					}
				}

				do_action('acf/create_field', array(
					'type'		=>	'select',
					'multiple'	=>	true,
					'name'		=>	$field['name'].'[geot_countries]',
					'value'		=>	$field['value']['geot_countries'],
					'choices'	=>	$choices
				));				
			
			} else { ?>
			
				<p> Add some countries first.</p>
			
			<?php	
			}							
		}
		?>			
		</div>
		<?php
	}
	
	
